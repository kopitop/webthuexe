     */
    public function show(Car $car, $slug)
    {
        try {
            preg_match(config('vars.regex.idFromSlug'), $slug, $matches);
            $id = $matches ? $matches[0] : null;
            $this->viewData['carsByCat'] = Category::findOrFail($id)->cars()->simplePaginate(3);
        } catch (Exception $e) {
            abort(404);
        }

        return view('client.category', $this->viewData);
    }

